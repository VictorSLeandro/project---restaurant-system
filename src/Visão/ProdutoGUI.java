    private void TabelaCarrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaCarrosMouseClicked
        try{
            int linha = TabelaCarros.getSelectedRow();
            //JOptionPane.showMessageDialog(null,"Linhas:"+ linha);
            Object objCodigo = TabelaCarros.getValueAt(linha, 0);
            Object objDesc = TabelaCarros.getValueAt(linha, 1);
            Object objcat = TabelaCarros.getValueAt(linha, 2);
            Object objpreco = TabelaCarros.getValueAt(linha, 3);
              ativaCampos();
       
              
       btnCadastra.setEnabled(false);
       btnAlterar.setEnabled(true);
       btnDeletar.setEnabled(true);
             
            TxtCod.setText(objCodigo.toString());
            TxtCat.setSelectedItem(objcat);
            TxtDesc.setText(objDesc.toString());
           TxtPreco.setText(objpreco.toString());
           prod.setCodProduto(Integer.parseInt(TxtCod.getText()));
           ImageIcon foto = CD.getFoto(prod);
           
           if (foto!=null){
               lblFoto.setIcon(CD.getFoto(prod));
                
               
           }
           else {
               lblFoto.setIcon(CD.getFoto(prod));
           }
          lblFoto.updateUI();
           
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null,"Erro ao carregar os dados"+e.getMessage());
        }
    }//GEN-LAST:event_TabelaCarrosMouseClicked

    private void TxtPesquisarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtPesquisarKeyReleased
        if(TxtPesquisar.getText().isEmpty()){
            try {
                conn.conecta();
                ResultSet rs = contr.listaProduto();
                DefaultTableModel modelo =  CD.carregarTabela(rs);
                TabelaCarros.setModel(modelo);
                TabelaCarros.setSelectionBackground(Color.yellow);
            }
            catch (Exception ex){
                JOptionPane.showMessageDialog(null,"Erro:");
            }
        }

        else{
            if (cbm_filtra.getSelectedIndex()==0){
                try {
                    prod.setCodProduto(Integer.parseInt(TxtPesquisar.getText()));
                    conn.conecta();
                    ResultSet rs = contr.buscarCodigoProduto(prod);
                    DefaultTableModel modelo =  CD.carregarTabela(rs);
                    TabelaCarros.setModel(modelo);
                    TabelaCarros.setSelectionBackground(Color.yellow);
                }
                catch (Exception ex){
                    JOptionPane.showMessageDialog(null,"Erro ao buscar dados:"+ex.getMessage());
                }
            }

            if (cbm_filtra.getSelectedIndex()==1){
                try {
                    prod.setCategoria((TxtPesquisar.getText()));
                    conn.conecta();
                    ResultSet rs = contr.buscarCategoriaProduto(prod);
                    DefaultTableModel modelo =  CD.carregarTabela(rs);
                    TabelaCarros.setModel(modelo);
                    TabelaCarros.setSelectionBackground(Color.yellow);
                }
                catch (Exception ex){
                    JOptionPane.showMessageDialog(null,"Erro ao buscar dados:"+ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_TxtPesquisarKeyReleased

